// ─── Evidence-First Architecture Interfaces ───────────────────────────────────

/** A single JD capability, extracted by browser LLM + normalized to a canonical ID. */
interface WorkspaceCapability {
  id: string;               // canonical, e.g. "owasp_top_10"
  name: string;             // display name, e.g. "OWASP Top 10"
  importance: "critical" | "high" | "medium" | "low";
  category: string;         // e.g. "cybersecurity"
}

/** A piece of evidence extracted from resume or user vault. */
interface UserEvidence {
  capabilityId: string;
  text: string;             // e.g. "Built Flask backend with JWT auth"
  source: "resume" | "vault";
  verified: boolean;
}

/** 4-state evidence coverage for one capability. */
type EvidenceState = "strong" | "weak" | "unknown" | "missing";
interface CapabilityCoverage {
  capability: WorkspaceCapability;
  state: EvidenceState;
  matchedEvidence: UserEvidence[];
  reasoning: string;
}

/** A targeted question generated by browser LLM for one unknown/missing capability. */
interface EvidenceQuestion {
  id: string;
  capabilityId: string;
  capabilityName: string;
  text: string;
  answer: string;
  stableKey: string;
}

/** A patch operation on the resume JSON (not a full rewrite). */
interface ResumePatch {
  section: "skills" | "summary" | string;  // e.g. "project_0", "experience_1"
  action: "add" | "enhance" | "rephrase";
  content: string;
  reasoning: string;
}

